package assignment01;

public class Company {

	//private fields
	private String name;
	private String city;
	
	//constructor
	public Company(String aName, String aCity) {
		name = aName;
		city = aCity;
	}

	//getters
	public String getName() {
		return name;
	}
	public String getCity() {
		return city;
	}
}
